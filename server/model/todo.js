let mongoose = require('mongoose');


// Create the todo schema
let Todo = mongoose.model('Todo', {
    text: {
        type: String,
        required: true,
        minlength: 1,
        trim: true
    },
    completed: {
        type: Boolean,
        default: false
    },
    completedAt: {
        type: Number,
        default: null
    } 
});


// Export the model
module.exports = {
    Todo
};